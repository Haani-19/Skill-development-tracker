    <h2>About Skill Development Tracker</h2>

<p>
The Skill Development Tracker is a web-based application designed to help users 
plan, organize, and monitor their learning activities. It allows users to add tasks 
related to different skills, assign deadlines, and track their completion status 
in an efficient way.
</p>

<p>
The system provides a structured approach to skill improvement by dividing learning 
into smaller tasks. Users can easily view their progress, identify pending work, 
and stay consistent with their goals.
</p>

<h3>Key Features:</h3>
<ul>
    <li>Add and manage tasks based on different skills</li>
    <li>Set deadlines for better time management</li>
    <li>Mark tasks as completed or pending</li>
    <li>View all tasks in a separate page</li>
    <li>Visual progress tracking using simple graphs</li>
</ul>

<h3>Technologies Used:</h3>
<ul>
    <li><strong>HTML</strong> – for structuring the web pages</li>
    <li><strong>CSS</strong> – for designing and styling the interface</li>
    <li><strong>PHP</strong> – for backend logic and data processing</li>
    <li><strong>XML</strong> – for storing predefined skill categories</li>
    <li><strong>MySQL</strong> – for storing and managing task data</li>
</ul>

<h3>Purpose:</h3>
<p>
The main objective of this project is to help students improve productivity by 
organizing their learning process. It encourages disciplined learning habits and 
provides a simple platform to track progress over time.
</p>
